alias ..="cd .."
alias ...="cd ../.."
alias .3="cd ../../.."
alias .4="cd ../../../.."
alias .5="cd ../../../../.."
alias .6="cd ../../../../../.."

alias rm="rm -iv"
alias cp="cp -iv"
alias mv="mv -iv"
alias mkdir="mkdir -pv"

alias ga="git add"
alias gb="git branch"
alias gc="git commit --verbose"
alias gcf="git config --list --show-origin --show-scope"
alias gd="git diff"
alias glo="git log --oneline --decorate --graph --all"
